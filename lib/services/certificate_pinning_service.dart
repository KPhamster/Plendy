/// Provides certificate-pinned HTTP clients for Google API endpoints.
///
/// Uses a [SecurityContext] that trusts only Google Trust Services root CAs,
/// rejecting connections signed by any other certificate authority. This
/// prevents man-in-the-middle attacks even if a rogue CA is trusted by the
/// device's system store.
///
/// On web platforms, pinning is a no-op (the browser handles TLS).
class CertificatePinningService {
  static final CertificatePinningService _instance =
      CertificatePinningService._internal();

  factory CertificatePinningService() => _instance;

  CertificatePinningService._internal();

  SecurityContext? _cachedContext;

  SecurityContext get _googleSecurityContext {
    if (_cachedContext != null) return _cachedContext!;

    final context = SecurityContext(withTrustedRoots: false);
    for (final pem in _trustedRootCAs) {
      context.setTrustedCertificatesBytes(utf8.encode(pem));
    }
    _cachedContext = context;
    return context;
  }

  /// Creates a [Dio] instance pinned to Google's root CAs.
  ///
  /// Use for all requests to `*.googleapis.com`, `*.firebaseio.com`,
  /// and other Google-operated endpoints.
  Dio createPinnedDio([BaseOptions? options]) {
    final dio = Dio(options);

    if (!kIsWeb) {
      dio.httpClientAdapter = IOHttpClientAdapter(
        createHttpClient: () => HttpClient(context: _googleSecurityContext),
      );
    }

    return dio;
  }

  /// Creates an [http.Client] pinned to Google's root CAs.
  http.Client createPinnedHttpClient() {
    if (kIsWeb) return http.Client();
    return IOClient(HttpClient(context: _googleSecurityContext));
  }

